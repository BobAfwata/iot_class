import math
# trigonometric functions
angle = 90 * math.pi / 180
print(math.sin(angle))
print(math.cos(angle))
print(math.tan(angle))

# logarithmic functions
num = 256
print(math.log(num, 10))

# power
print(math.pow(2, 4))
print(math.sqrt(256))